    void cargarReservaciones(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Reservacion r : reservaciones){
            model.addRow(new Object[]{
                r.getId(),
                r.getCliente().getId(),
                r.getCliente().getNombre(),
                r.getCliente().getTelefono(),
                r.getMesa().getId()
            });
        }
    }

